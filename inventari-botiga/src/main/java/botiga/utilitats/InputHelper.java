package main.java.botiga.utilitats;

public class InputHelper {
    // La classe InputHelper es dissenya per facilitar la lectura de dades de l'usuari des de la consola.
    // Aquest tipus de classe és molt útil quan tens diverses parts del codi on necessites llegir dades del teclat.
    // En lloc de repetir el mateix codi cada vegada que has de fer una lectura, la classe InputHelper centralitza
    // la lògica de validació de les entrades de l'usuari i la fa reutilitzable.  Aquesta classe també ajuda a evitar
    // errors d'entrada com quan l'usuari escriu caràcters en lloc de nombres, o quan introdueix valors que no compleixen
    // amb el format esperat (per exemple, un número negatiu quan s'espera un valor positiu).
}
